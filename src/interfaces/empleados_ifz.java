    private void BTNactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNactualizarActionPerformed
        
        persona_servicio empleado=new persona_servicio();
        
        boolean insetado=false;
        
        if (TXTidEmpleado.getText().equals("")){  //Se inserta
            
            empleado.setApellidoPaterno(TXTapellidoPaterno.getText());
            empleado.setApellidoMaterno(TXTapellidoMaterno.getText());
            empleado.setNombre(TXTnombres.getText());
            empleado.setIdTipoPersona(2);
            
            insetado=empleado.add();
            
            if (insetado){
               agrupacion.limpiaTodosLosCampos();
                BTNactualizar.setText("Agregar nuevo empleado");
               
            }else{
                javax.swing.JOptionPane.showMessageDialog(null, "No pudo ser insetado el empleado");
            }
            
        }
        else{ //Se guarda
        
            empleado.setIdPersona(Integer.parseInt(TXTidEmpleado.getText()));
            empleado.setApellidoPaterno(TXTapellidoPaterno.getText());
            empleado.setApellidoMaterno(TXTapellidoMaterno.getText());
            empleado.setNombre(TXTnombres.getText());
            empleado.setIdTipoPersona(2);
            
            insetado=empleado.update();
            
            if (insetado){
               agrupacion.limpiaTodosLosCampos();
                BTNactualizar.setText("Agregar nuevo empleado");
            }else{
                javax.swing.JOptionPane.showMessageDialog(null, "No pudo ser actualizado el empleado");
            }
            
        }
        
       
       
        TBLDTMempleados = empleado.LISTAempleados();
        TBLEemplados.setModel(TBLDTMempleados);
        
        
        
    }//GEN-LAST:event_BTNactualizarActionPerformed

