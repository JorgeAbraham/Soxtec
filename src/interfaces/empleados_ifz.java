    private void TBLEempladosMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBLEempladosMouseReleased
        if (TBLEemplados.getSelectedRow()!=-1){
            persona_servicio empleado=new persona_servicio();
            empleado.setIdPersona(Integer.parseInt(TBLDTMempleados.getValueAt(TBLEemplados.getSelectedRow(), 1)+""));
            empleado.get();
            TXTidEmpleado.setText(empleado.getIdPersona()+"");
            TXTapellidoPaterno.setText(empleado.getApellidoPaterno());
            TXTapellidoMaterno.setText(empleado.getApellidoMaterno());
            TXTnombres.setText(empleado.getNombre());
            BTNactualizar.setText("Actualiza informaci√≥n de empleado");
            
        }
        
    }//GEN-LAST:event_TBLEempladosMouseReleased

